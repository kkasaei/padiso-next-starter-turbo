// Function to get the icon image source (returns string path for <img> tags)
export const getIconSrc = (
  iconName: string,
  variant: 'color' | 'text' | '' = 'color'
) => {
  const suffix = variant ? `-${variant}` : '';
  return `/icons/${iconName}${suffix}.svg`;
};

// Function to get icon by index with variant
export const getIconByIndex = (
  index: number,
  variant: 'color' | 'text' | '' = 'color'
) => {
  const iconName = getIconName(index);
  return getIconSrc(iconName, variant);
};

// Function to get the actual imported icon (for direct use in components)
export const getImportedIcon = (iconName: string) => {
  return iconMap[iconName] || null;
};

// Function to get imported icon by index
export const getImportedIconByIndex = (index: number) => {
  const iconName = getIconName(index);
  return getImportedIcon(iconName);
};
